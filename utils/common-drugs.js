const seen = new Set()
const commonDrugs = []

Object.entries(CATEGORY_DRUGS).forEach(([category, entries]) => {
  entries.forEach((entry) => {
    const [genericName, tradeName = '', aliases = ''] = entry.split('|')
    if (!genericName || seen.has(genericName)) return
    seen.add(genericName)
    const safety = SAFETY_OVERRIDES[genericName] || ['', '']
    commonDrugs.push({
      drug_id: `Dsys${String(commonDrugs.length + 1).padStart(4, '0')}`,
      generic_name: genericName,
      trade_name: tradeName,
      aliases,
      category,
      category_label: CATEGORY_LABELS[category],
      ingredient: genericName,
      dosage_text: '',
      contraindication_note: safety[0],
      interaction_note: safety[1],
    })
  })
})

